def build_records() -> list[dict[str, Any]]:
    records = []
    for team_code, players in SQUADS.items():
        seen_numbers: set[int | None] = set()
        seen_names: set[str] = set()
        for full_name, known_as, position, shirt_number in players:
            # Deduplicate by full_name within team (data errors above)
            if full_name in seen_names:
                continue
            seen_names.add(full_name)
            # Deduplicate shirt numbers — keep first occurrence
            if shirt_number in seen_numbers:
                shirt_number = None
            if shirt_number is not None:
                seen_numbers.add(shirt_number)
            records.append(
                {
                    "team_code": team_code,
                    "full_name": full_name,
                    "known_as": known_as,
                    "position": position,
                    "shirt_number": shirt_number,
                }
            )
    return records


